    void tableDesign() {
        JTableHeader th = jTable2.getTableHeader();
        Font font = new Font("Century Gothic", Font.PLAIN, 14);
        th.setFont(font);
        th.setOpaque(false);
        Color cb = new Color(51, 51, 58);
        Color cf = new Color(241, 242, 246);
        th.setForeground(cf);
        th.setBackground(cb);

    }

    void tableLoad() {
        System.out.println("table loading");
        TableModel model = jTable2.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;
        dtm.setRowCount(0);

        try {
            ResultSet rs = search("SELECT * FROM user WHERE status = '1';");
            int i = 1;

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("full_name"));
                v.add(rs.getString("emp_id"));
                v.add(rs.getString("email"));
                v.add(rs.getString("contact_no"));
                v.add(rs.getString("user_role"));
                v.add(rs.getString("registerd_date"));

                SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");
                try {
                    Date date = sdf1.parse(rs.getString("registerd_date"));

                    jDateChooser2.setDate(date);

                    LocalDate localDate1 = LocalDate.parse(rs.getString("registerd_date"));
                    LocalDate localDate2 = LocalDate.now();

                    long years = java.time.temporal.ChronoUnit.YEARS.between(localDate1, localDate2);
                    
                    v.add(years);

                } catch (ParseException ex) {
                    ex.printStackTrace();
                }

                dtm.addRow(v);

                jLabel18.setText(String.valueOf(i));
                i++;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

