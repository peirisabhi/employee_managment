    private void mouseClick(JPanel p) {

        JPanel panelArray[] = new JPanel[4];
        panelArray[0] = jPanel6;
        panelArray[1] = jPanel9;
        panelArray[2] = jPanel13;
        panelArray[3] = jPanel14;

        Color c1 = new Color(240, 240, 240);
        Color c2 = new Color(199, 199, 199);

        for (int x = 0; x < panelArray.length; x++) {
            panelArray[x].setBackground(c2);
            if (panelArray[x] == p) {
                panelArray[x].setBackground(c1);
            }
        }

    }

